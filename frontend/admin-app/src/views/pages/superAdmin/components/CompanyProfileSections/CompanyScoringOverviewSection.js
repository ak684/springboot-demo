import { Box } from '@mui/material';
import BaseSection from './BaseSection';
import ScoringOverviewMagnitude from '../../../scoringOverview/containers/ScoringOverviewMagnitude';
import ScoringOverviewLikelihood from '../../../scoringOverview/containers/ScoringOverviewLikelihood';
import CompanyScoringOverviewBubble from '../CompanyScoringOverviewBubble';
import CompanyScoringOverviewTotalChart from '../CompanyScoringOverviewTotalChart';
import CompanyScoringOverviewImpactList from '../CompanyScoringOverviewImpactList';
